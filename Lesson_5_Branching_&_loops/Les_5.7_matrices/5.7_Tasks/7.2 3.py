"""
Поменять местами элементы главной и побочной диагоналей квадратной матрицы.

---------
главная диагональ i == j
побочная диагональ i + j + 1 == N
"""
matrix = [[1, -4, 6, 5],
          [-6, 3, 0, -8],
          [2, 9, -4, 2],
          [-3, 6, -1, 9]]

N = len(matrix)

for i in range(0, N):
    matrix[i][i], matrix[i][N-i-1] = matrix[i][N-i-1], matrix[i][i]

print(matrix)
